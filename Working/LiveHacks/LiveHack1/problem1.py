# find the temperature in celsius
celsius = float(input("What is the current temperature in celsius?  "))

# compute celsius into fahrenheit
fahrenheit = 9/5 * celsius + 32

# output in fahrenheit
print("It is ", fahrenheit, " degrees fahrenheit right now. ")
