# Find the current temperature in Fahrenheit
Fahrenheit = float(input("What is the temperature in Fahrenheit?: "))

# compute celsius from fahrenheit
celcius = 5/9 * (Fahrenheit-32)

# Output in celcius
print("The temperature in celcius is: ", celcius)
